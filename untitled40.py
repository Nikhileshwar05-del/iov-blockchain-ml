import random
import numpy as np
import pandas as pd

SEED = 42
random.seed(SEED)
np.random.seed(SEED)

ROUNDS = 5
NUM_VEHICLES = 200

vehicles = {}


for i in range(1, NUM_VEHICLES + 1):

    vid = f"V{i}"

    label = random.randint(0,3)

    if label == 0:
        trust = random.uniform(0.7,0.9)
    elif label == 1:
        trust = random.uniform(0.5,0.75)
    elif label == 2:
        trust = random.uniform(0.25,0.55)
    else:
        trust = random.uniform(0.05,0.3)

    vehicles[vid] = {
        "label": label,
        "trust": trust
    }

# RSU class
class RSU:

    def __init__(self,name):
        self.name = name
        self.buffer = []

    def receive_from_vehicle(self,packet):
        self.buffer.append(packet)

    def forward_to_vehicle(self):
        return random.randint(1,5)


rsu1 = RSU("RSU1")
rsu2 = RSU("RSU2")
rsu3 = RSU("RSU3")

vehicle_ids = list(vehicles.keys())

RSU_MAP = {
    rsu1: vehicle_ids[:67],
    rsu2: vehicle_ids[67:134],
    rsu3: vehicle_ids[134:]
}

simulation_data = []

print("\nIoV Simulation Start\n")

for round_num in range(1,ROUNDS+1):

    for rsu,vehicle_list in RSU_MAP.items():

        for vid in vehicle_list:

            label = vehicles[vid]["label"]
            trust = vehicles[vid]["trust"]

            forwarded_messages = rsu.forward_to_vehicle()
            consensus = random.choice([0,1])

            # feature generation
            if label == 0:
                msg_rate = np.random.normal(12,5)
                false_ratio = np.random.normal(0.12,0.07)
                drop = np.random.normal(0.05,0.04)
                delay = np.random.normal(0.4,0.2)

            elif label == 1:
                msg_rate = np.random.normal(18,6)
                false_ratio = np.random.normal(0.28,0.12)
                drop = np.random.normal(0.15,0.06)
                delay = np.random.normal(0.7,0.3)

            elif label == 2:
                msg_rate = np.random.normal(24,7)
                false_ratio = np.random.normal(0.45,0.15)
                drop = np.random.normal(0.30,0.08)
                delay = np.random.normal(1.1,0.4)

            else:
                msg_rate = np.random.normal(30,8)
                false_ratio = np.random.normal(0.65,0.18)
                drop = np.random.normal(0.45,0.10)
                delay = np.random.normal(1.7,0.5)

            # CLIP VALUES
            msg_rate = max(1, msg_rate)
            false_ratio = min(1, abs(false_ratio))
            drop = min(1, abs(drop))
            delay = abs(delay)

            #  NORMALIZATION
            norm_msg = min(msg_rate / 30, 1)
            norm_false = false_ratio
            norm_drop = drop
            norm_delay = min(delay / 2, 1)
            norm_forward = forwarded_messages / 5
            norm_consensus = consensus

            # TRUST USING ALL PARAMETERS
            impact = (
                norm_msg +
                (1 - norm_false) +
                (1 - norm_drop) +
                (1 - norm_delay) +
                norm_forward +
                norm_consensus
            ) / 6

            # update trust smoothly
            trust = (trust + impact) / 2

            trust = max(0, min(1, trust))
            vehicles[vid]["trust"] = trust

            packet = {
                "Round": round_num,
                "Vehicle_ID": vid,
                "RSU": rsu.name,

                "RSU_Forwarded_Messages": forwarded_messages,

                "Message_Rate": msg_rate,
                "False_Message_Ratio": false_ratio,
                "Packet_Drop_Rate": drop,
                "Validation_Delay": delay,

                "Trust_Score": trust,
                "Consensus_Participation": consensus,

                "Label": label
            }

            rsu.receive_from_vehicle(packet)
            simulation_data.append(packet)

df = pd.DataFrame(simulation_data)
df.to_csv("iov_dataset_fixed.csv",index=False)

print("Simulation Complete:",len(df))

