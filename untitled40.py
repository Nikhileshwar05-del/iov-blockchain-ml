import hashlib
import time
import pandas as pd
import random

# Load dataset
df = pd.read_csv("iov_dataset_fixed.csv")

RSUs = ["RSU1", "RSU2", "RSU3"]

DIFFICULTY = 2
blockchain = []

# -----------------------
# Hash Function
# -----------------------
def hash_block(block):
    return hashlib.sha256(str(block).encode()).hexdigest()

# -----------------------
# Proof-of-Work Mining
# -----------------------
def pow_mine(block):
    prefix = "0" * DIFFICULTY
    nonce = 0
    start_time = time.time()

    while True:
        block["nonce"] = nonce
        hash_value = hash_block(block)

        if hash_value.startswith(prefix):
            mining_time = time.time() - start_time
            return hash_value, nonce, mining_time

        nonce += 1

# -----------------------
# PRE-VALIDATION (Majority Voting)
# -----------------------
def pre_validate(block, validators):

    votes = []

    for v in validators:

        # Simple rule-based validation
        if (
            block["false_ratio"] < 0.6 and
            block["drop"] < 0.5 and
            block["delay"] < 2
        ):
            votes.append(1)
        else:
            votes.append(0)

    return sum(votes) > len(votes) // 2

# -----------------------
# POST-VALIDATION
# -----------------------
def post_validate(block, block_hash, prev_hash, validators):

    votes = []

    for v in validators:

        valid_hash = block_hash.startswith("0" * DIFFICULTY)
        correct_link = block["previous_hash"] == prev_hash

        if valid_hash and correct_link:
            votes.append(1)
        else:
            votes.append(0)

    return sum(votes) > len(votes) // 2

# -----------------------
# GENESIS BLOCK
# -----------------------
genesis = {
    "previous_hash": "0",
    "timestamp": time.time(),
    "difficulty_level": DIFFICULTY
}

h, n, t = pow_mine(genesis)

genesis["hash"] = h
genesis["nonce"] = n
genesis["mining_time"] = t
genesis["mined_by"] = "RSU_GENESIS"

blockchain.append(genesis)
prev_hash = h

print("\nBlockchain Start (Hybrid Consensus)\n")

# -----------------------
# MAIN LOOP
# -----------------------
for i, row in df.iterrows():

    proposer = random.choice(RSUs)
    validators = [r for r in RSUs if r != proposer]
    miner = random.choice(RSUs)

    block = {
        "vehicle": row["Vehicle_ID"],
        "round": row["Round"],
        "rsu": row["RSU"],

        "msg_rate": row["Message_Rate"],
        "false_ratio": row["False_Message_Ratio"],
        "drop": row["Packet_Drop_Rate"],
        "delay": row["Validation_Delay"],
        "trust": row["Trust_Score"],

        "proposed_by": proposer,
        "validated_by": validators,

        "difficulty_level": DIFFICULTY,
        "previous_hash": prev_hash,
        "timestamp": time.time()
    }

    # -----------------------
    # STEP 1: PRE-VALIDATION
    # -----------------------
    if not pre_validate(block, validators):
        continue  # Reject block before mining

    # -----------------------
    # STEP 2: MINING (PoW)
    # -----------------------
    block_hash, nonce, mining_time = pow_mine(block)

    block["hash"] = block_hash
    block["nonce"] = nonce
    block["mining_time"] = round(mining_time, 4)
    block["mined_by"] = miner

    # -----------------------
    # STEP 3: POST-VALIDATION
    # -----------------------
    if post_validate(block, block_hash, prev_hash, validators):

        blockchain.append(block)
        prev_hash = block_hash

    else:
        continue  # Reject after mining

# -----------------------
# FINAL OUTPUT
# -----------------------
blockchain_df = pd.DataFrame(blockchain)

